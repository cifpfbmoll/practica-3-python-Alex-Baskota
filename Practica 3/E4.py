#Pide al usuario que introduzca 3 calificaciones, y calcula la media de estas.

calif1 = float(input("Introduzca la primera calificación: "))
calif2 = float(input("Introduzca la segunda calificación: "))
calif3 = float(input("Introduzca la tercera calificación: "))

media = (calif1+calif2+calif3)/3

print("Su nota media es de",media)